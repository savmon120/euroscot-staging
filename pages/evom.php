      <h3>Learn More...</h3>
      <p>This document serves to provide virtual pilots operating on behalf of Euroscot/Air Scotland/Air Scotland Cargo Virtual with operating and membership information relating to both airlines' operations.</p><br>
    <p>Euroscot & Air Scotland Virtual operates a <strong>Just Culture</strong> policy. In aviation, a Just Culture policy promotes safety by encouraging open reporting of errors and incidents without fear of punishment for honest mistakes. This means, that within the virtual airline, if you believe you have carried out an action outwith this EVOM, that as long as you openly justify and explain — e.g. a PIREP following a hard landing or unstable approach — and add comments/notes within Pegasus prior to filing, you will never be penalised or face formal action.
    Instead, you will be listened to and, if appropriate, feedback and coaching to help improve or avoid such situations occurring again will be given. The learnings of these events will be used (where appropriate) to enhance operations and efficiency for all pilots in the VA.</p>

