<!-- Section 4: Scheduling & Duty Hours -->
<div class="dropdown-box">
  4. Scheduling & Duty Hours</div>
  <div class="dropdown-content">
    <h4>4.1 Scheduling Overview</h4>
    <p>Euroscot Virtual and Air Scotland Virtual adopt a pilot-led scheduling policy. While we offer a structured timetable, we understand the importance of flexibility for our members.</p><br>
    <ul>
      <li><strong>Scheduled Flights:</strong> Published via VA schedule or SimBrief auto-briefing</li>
      <li><strong>Seasonal Operations:</strong> Available only during specific months or events</li>
      <li><strong>Charter Operations:</strong> Operated as part of a charter arrangement</li>
    </ul><br>
    <p style="text-align: center; font-weight: bold;">
  <em>We currently operate a year-round domestic and cargo operation in addition to our summer and winter schedules.</em>
</p>

    <br>

    <h4>4.2 Flight Assignment & Self-Dispatch</h4>
    <ul>
      <li>Pilots choose flights based on aircraft and availability</li>
      <li>Dynamic scheduling updates based on demand and realism</li>
      <li>Realism-based routes (e.g. EU regional for Euroscot Virtual, UK domestic/long-haul for Air Scotland Virtual and cargo operations under Air Scotland Virtual) are non-negotiable</li>
      <li>Seasonal and charter flights listed via Discord, email, or website</li>
    </ul>

