    </ul>

    <h4>4.4 Schedule Publication</h4>
    <ul>
      <li><strong>Euroscot Virtual (S6):</strong> European operations</li>
      <li><strong>Air Scotland Virtual (S7):</strong> Domestic and long-haul operations</li>
      <li><strong>Air Scotland Cargo Virtual (S8):</strong> All cargo operations</li>
      <li>Timetables accessed via VAMSYS crew portal or dispatch sheets</li>
      <li>Updates weekly, usually over weekends</li>
      <li>Route suggestions welcome via form or Discord</li>
      <li>Real-time NOTAMs and special assignments posted on Discord</li>
    </ul>
