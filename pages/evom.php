<!-- Section 8: Standard Operating Procedures (SOPs) -->
<div class="dropdown-box">
  8. Standard Operating Procedures (SOPs)</div>
  <div class="dropdown-content">
    <h4>8.1 Flight Planning</h4>
    <ul>
      <li>Select appropriate aircraft type/variant for departure and arrival aerodromes</li>
      <li>Ensure competence on aircraft type and variant</li>
      <li>Use SimBrief or equivalent dispatch system to generate OFPs</li>
      <li>Confirm routing aligns with real-world airway structure</li>
      <li>Check NOTAMs, METAR/TAF, and airspace restrictions before every flight</li>
      <li>Inform ATC if flying conventional navigation (VOR/NDB)</li>
    </ul>

    <h4>8.2 Pre-Flight Procedure</h4>
    <ul>
      <li>Load Pegasus ACARS and confirm active status</li>
      <li>Load aircraft at correct gate/stand</li>
      <li>Configure route, weights, and fuel</li>
      <li>Connect to appropriate online network</li>
      <li>Follow all instructions given by ATC if present</li>
    </ul>
<p style="text-align: center;">
  <strong><em>“Think like a real-world pilot: be punctual, prepared, and fully briefed before pushback.”</em></strong>
</p>

    <h4>8.3 Departure & Enroute</h4>
    <ul>
      <li>Comply with ATC clearance limits and SID procedures</li>
      <li>Maintain assigned altitudes, speeds, and headings</li>
      <li>File pilot remarks that include Euroscot Virtual / Air Scotland / Air Scotland Cargo callsigns and our VA website URL</li>
      <li>Monitor Pegasus status throughout</li>
      <li>Remain on Unicom (122.800) and report intentions if no ATC coverage</li>
      <li>Guard frequency monitoring (121.500) recommended</li>
    </ul>

    <h4>8.4 Arrival & Shutdown</h4>
    <ul>
      <li>Comply with STAR and arrival procedures</li>
      <li>Ensure Pegasus records landing with proper ICAO</li>
      <li>Use assigned gates/stands unless otherwise directed</li>
      <li>Follow correct shutdown procedure</li>
      <li>Submit your PIREP</li>
    </ul>

    <h4>8.5 Simulation Conduct</h4>
    <ul>
      <li>No time acceleration permitted</li>
      <li>Pause function must be noted in ACARS remarks</li>
      <li>Avoid unrealistic vertical speeds or fuel burn</li>
      <li>No mid-flight aircraft changes or repositioning</li>
      <li>No excessive slewing or skipping sectors</li>
    </ul>

    <h4>8.6 Emergencies & Disruptions</h4>
    <ul>
      <li>Emergency situations must be logged in PIREP remarks</li>
      <li>If diversion occurs, update Pegasus or notify staff</li>
      <li>Intentional crashes, prank callsigns, or joke scenarios will result in disciplinary action</li>
    </ul>
